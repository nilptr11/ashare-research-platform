def _financial_recipe(
    recipe_id: str,
    source_api: str,
    target_dataset_id: str,
    *,
    use_period_param: bool = True,
    extra_params: dict[str, str] | None = None,
    field_map: dict[str, str] | None = None,
    notes: str = "",
) -> IngestionRecipe:
    params_template = {"ts_code": "${partition.security_id}"}
    if use_period_param:
        params_template["period"] = "${partition.period}"
    params_template.update(extra_params or {})
    return IngestionRecipe(
        id=recipe_id,
        source_id="tushare",
        source_api=source_api,
        target_dataset_id=target_dataset_id,
        schedule="ashare_financials_on_demand",
        params_template=params_template,
        fields=(),
        field_map={
            "ts_code": "security_id",
            "end_date": "period",
            "type": "forecast_type",
            **(field_map or {}),
        },
        lineage=LineagePolicy(raw_required=True, staging_required=True),
        selection_priority=20,
        notes=notes,
    )


