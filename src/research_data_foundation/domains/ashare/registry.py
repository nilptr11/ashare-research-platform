def _financial_temporal() -> TemporalPolicy:
    return TemporalPolicy(
        temporal_mode="filing",
        finality="final",
        available_after="delayed",
        as_of_policy="latest_before",
    )


def _financial_usage(*, permits_company_exposure: bool = False) -> UsagePolicy:
    allowed = ("financial_analysis", "evidence", "market_context")
    if permits_company_exposure:
        allowed = (*allowed, "company_business_exposure")
    return UsagePolicy(
        allowed_uses=allowed,
        forbidden_uses=("trade_execution",),
    )


def _financial_dataset(
    dataset_id: str,
    title: str,
    *,
    required_tail: tuple[str, ...],
    analysis_columns: tuple[str, ...],
    permits_company_exposure: bool = False,
    units: dict[str, str] | None = None,
) -> DatasetContract:
    return DatasetContract(
        id=dataset_id,
        title=title,
        domain="ashare_financials",
        market_scope="cn_ashare",
        role="financial_fact",
        temporal=_financial_temporal(),
        partition_keys=("period", "security_id"),
        primary_key=("period", "security_id", "ann_date"),
        required_columns=("period", "security_id", "ann_date", *required_tail),
        usage=_financial_usage(permits_company_exposure=permits_company_exposure),
        units=units or {},
        analysis_columns=analysis_columns,
        empty_policy="allow_empty",
    )


